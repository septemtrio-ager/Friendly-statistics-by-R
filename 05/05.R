A <- rnorm(50)
B <- rnorm(50)
C <- A * 0.5 + sqrt(0.75) * B
自尊感情 <- 10 * A + 50
ソーシャルスキル <- 1 * C + 50

plot(自尊感情, ソーシャルスキル, xlim = c(0, 100), ylim = c(0, 100))
points(50, 50, cex = 45)
legend(50, 10, legend = "抽出された標本に含まれるデータ", pch = 1)
legend(10, 100, legend = "母集団(円の中全体にデータが散らばっていて相関はゼロ)")
