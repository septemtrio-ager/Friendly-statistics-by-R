# 2.8 分散・標準偏差とは

# ======================================

テストA平均 <- mean(テストA)
テストA平均

平均からの偏差 <- テストA - テストA平均
平均からの偏差

平均からの偏差の二乗 <- 平均からの偏差 ^ 2
平均からの偏差の二乗

平均からの偏差の二乗和 <- sum(平均からの偏差の二乗)
平均からの偏差の二乗和

データ数 <- length(テストA)
データ数

分散 <- 平均からの偏差の二乗和 / データ数 # 分散を計算する
分散

sqrt(分散) # 標準偏差を計算する

var(テストA) # var() を使って"不偏"分散を計算する

不偏分散 <- var(テストA)
不偏分散

標本分散 <- 不偏分散 * (データ数 - 1) / データ数
標本分散

sd(テストA) # sd() を使って"不偏"分散の平方根(標準偏差)を計算する

sqrt(sd(テストA) ^ 2 * (データ数 - 1) / データ数) # 標本分散の平方根

sqrt(標本分散)

