# ======================================
#
# 2.10 標準化
#
# ======================================

心理学テスト
心理学平均 <- mean(心理学テスト)
心理学平均

心理学標準偏差 <- sqrt(mean((心理学テスト - 心理学平均) ^ 2))
心理学標準偏差

心理学z得点 <- (心理学テスト - 心理学平均) / 心理学標準偏差
心理学z得点

心理学z得点平均 <- mean(心理学z得点)
心理学z得点平均

心理学z得点標準偏差 <- sqrt(mean((心理学z得点 - 心理学z得点平均) ^ 2))
心理学z得点標準偏差

# ======================================
#
